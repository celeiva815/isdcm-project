    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getVideoscol() {
        return videoscol;
    }

    public void setVideoscol(String videoscol) {
        this.videoscol = videoscol;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

